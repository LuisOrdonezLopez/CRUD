    void listar() {
        String sql = "Select * from ask";
        try {
            cn = con.getConnection();
            st = cn.createStatement();
            rs = st.executeQuery(sql);

            Object[] usuario = new Object[9];
            modelo = (DefaultTableModel) Tabla_Datos.getModel();

            while (rs.next()) {
                usuario[0] = rs.getInt("identifier");
                usuario[1] = rs.getString("name");
                usuario[2] = rs.getString("email");
                usuario[3] = rs.getString("ask");
                usuario[4] = rs.getString("question_date");
                usuario[5] = rs.getString("adviser");
                usuario[6] = rs.getString("answer");
                usuario[7] = rs.getString("query_status");
                modelo.addRow(usuario);

            }

            Tabla_Datos.setModel(modelo);

        } catch (SQLException e) {
            System.out.println("Error en : " + e);
        }
    }
    
    void agregar() {

        String nombre = txtnombre.getText();
        String email = txtemail.getText();
        String pregunta = txtpregunta.getText();
        String fecha_pregunta = txtfecha_pregunta.getText();
        String asesor = txtasesor.getText();
        String respuesta = txtrespuesta.getText();
        String estado_pregunta = txtestado_pregunta.getSelectedItem().toString();

        if (nombre.equals("") || email.equals("") || pregunta.equals("") || fecha_pregunta.equals("") || asesor.equals("") || respuesta.equals("") || estado_pregunta.equals("")) {
            JOptionPane.showMessageDialog(null, "Completar todos los campos");
            return;
        }

        String sql = "INSERT INTO ask (name, email, ask, question_date, adviser, answer, query_status) "
                + "VALUES (?, ?, ?, ?, ?, ?, ?)";

        try {
            cn = con.getConnection();
            PreparedStatement pst = cn.prepareStatement(sql);
            pst.setString(1, nombre);
            pst.setString(2, email);
            pst.setString(3, pregunta);
            pst.setString(4, fecha_pregunta);
            pst.setString(5, asesor);
            pst.setString(6, respuesta);
            pst.setString(7, estado_pregunta);

            int n = pst.executeUpdate();
            if (n > 0) {
                JOptionPane.showMessageDialog(null, "Registro agregado exitosamente");
                limpiar();
                listar();
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Error en: " + e.getMessage());
        }
    }
    
    void limpiar() {
        while (Tabla_Datos.getRowCount() > 0) {
            modelo.removeRow(0);
        }
    }
    
    private void eliminar() {
        int fila = Tabla_Datos.getSelectedRow();
        if (fila == -1) {
            JOptionPane.showMessageDialog(null, "Selecciona a alguien");
        } else {

            int identificador = Integer.parseInt(Tabla_Datos.getValueAt(fila, 0).toString());
            String sql = "DELETE FROM ask WHERE identifier = " + identificador;
            try {
                cn = Conexion.getConnection();
                st = cn.createStatement();
                st.executeUpdate(sql);
                JOptionPane.showMessageDialog(null, "Pregunta eliminada");
                limpiar();
                listar();

            } catch (SQLException e) {
                System.out.println("Error en: " + e);
            }
        }
    }
    
