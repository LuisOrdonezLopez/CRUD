    private void editar() {
        int fila = Tabla_Datos.getSelectedRow();
        if (fila == -1) {
            JOptionPane.showMessageDialog(null, "Selecciona a alguien para modificar.");
            return;
        }

        int identificador = (int) Tabla_Datos.getValueAt(fila, 0); // Suponiendo que el ID es la primera columna
        String nombre = txtnombre.getText();
        String email = txtemail.getText();
        String pregunta = txtpregunta.getText();
        String fecha_pregunta = txtfecha_pregunta.getText();
        String asesor = txtasesor.getText();
        String respuesta = txtrespuesta.getText();
        String estado_pregunta = txtestado_pregunta.getSelectedItem().toString();

        if (nombre.isEmpty() || email.isEmpty() || pregunta.isEmpty() || fecha_pregunta.isEmpty() || asesor.isEmpty() || respuesta.isEmpty() || estado_pregunta.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Todos los campos son obligatorios.");
            return;
        }

        String sql = "UPDATE ask SET name = ?, email = ?, ask = ?, question_date = ?, adviser = ?, answer = ?, query_status = ? WHERE identifier = ?";

        try {
            cn = Conexion.getConnection();
            PreparedStatement pst = cn.prepareStatement(sql);
            pst.setString(1, nombre);
            pst.setString(2, email);
            pst.setString(3, pregunta);
            pst.setString(4, fecha_pregunta);
            pst.setString(5, asesor);
            pst.setString(6, respuesta);
            pst.setString(7, estado_pregunta);
            pst.setInt(8, identificador);

            int n = pst.executeUpdate();
            if (n > 0) {
                JOptionPane.showMessageDialog(null, "Actualización completada");
                limpiar();
                listar(); // Asumiendo que este método lista los datos en la tabla
            } else {
                JOptionPane.showMessageDialog(null, "No se encontró la entrada a modificar.");
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Error en: " + e.getMessage());
        }
    }

